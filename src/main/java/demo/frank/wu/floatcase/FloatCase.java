package demo.frank.wu.floatcase;

public class FloatCase {
	public static void main(String[] args) {
		float x = 1.34f;
		float y = 1.36f;
		
		float z = x+y;
		System.err.println(z);
		float m = 5.34f;
		System.err.println(m-z);
		
		float a = 17.75552f;
		float b = 16.15552f;
		float c = a-b;
		System.err.println(c);
	}
}
